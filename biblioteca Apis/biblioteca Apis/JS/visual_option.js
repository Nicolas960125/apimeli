//Funci√≥n que lanza el copiado del c√≥digo
function copiarAlPortapapeles(ev) {
    var codigoACopiar = document.getElementById('textoACopiar'); //Elemento a copiar
    //Debe estar seleccionado en la p√°gina para que surta efecto, as√≠ que...
    var seleccion = document.createRange(); //Creo una nueva selecci√≥n vac√≠a
    seleccion.selectNodeContents(codigoACopiar); //incluyo el nodo en la selecci√≥n
    //Antes de a√±adir el intervalo de selecci√≥n a la selecci√≥n actual, elimino otros que pudieran existir (sino no funciona en Edge)
    window.getSelection().removeAllRanges();
    window.getSelection().addRange(seleccion); //Y la a√±ado a lo seleccionado actualmente
    try {
        var res = document.execCommand('copy'); //Intento el copiado
        if (res)
            exito();
        else
            fracaso();

        mostrarAlerta();
    } catch (ex) {
        excepcion();
    }
    window.getSelection().removeRange(seleccion);
}